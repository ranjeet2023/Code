                                        languages HTML, CSS, and JavaScript is a bonus.</p>
                                </li>
                            </ul>
                            <ul class="mt-5">
                                <li>
                                    <p><b>Frameworks: </b>Frameworks are the libraries of back-end
                                        programming languages that help to build the server configuration.
                                        They tend to be linked with programming languages, so if you are
                                        familiar with Python, you’ll also know Flask, Django, or another
                                        Python-based framework, and so on.</p>
                                </li>
                            </ul>
                            <ul class="mt-5">
                                <li>
                                    <p><b>Databases and servers: </b>You’ll need to understand how to stack
                                        and recover data from databases, as back-end programming controls
                                        access to this information, including storage and recovery. MongoDB
                                        and MySQL are popular database programs. The database stores and
                                        organizes the client’s data so that it can be easily arranged and
                                        recovered, just like you might use cloud storage for your photos.
                                        This database then runs on a server that provides data upon request.
                                    </p>
                                </li>
                            </ul>
                            <ul class="mt-5">
                                <li>
                                    <p><b>Application Program Interface (API): </b>An API is a series of
                                        definitions and rules for developing application software. In
                                        addition to internet browser websites, companies often want a mobile
                                        app for iOS or Android. Knowledge of application-building languages
                                        like JavaScript will expand your job opportunities.&nbsp;</p>
