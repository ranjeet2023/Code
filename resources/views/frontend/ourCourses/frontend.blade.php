                                <h2 id="3-how-to-become-a-front-end-developer" class="mt-5">How to become a front-end developer</h2>
                                <p class="mt-5">A career as a front-end web developer can flex your creativity and problem-solving
                                    skills. As a field that is constantly evolving to incorporate new technology, front-end
                                    development can reward those who like to learn new things and face challenges. The next
                                    few sections outline some of the most prominent skills for front-end developers.</p>
                                <h3 class="mt-5">HTML, CSS, and JavaScript</h3>
                                <p class="mt-5">These three languages are essential to anyone who wants to work in front-end development.
                                </p>
                                <p></p>
                                <ul>
                                    <li>
                                        <p><b>HTML (hypertext markup language): </b>The basic building block needed to
                                            develop websites; a language that allows you to make notes in digital documents
                                            that are different from regular text.</p>
                                    </li>
                                </ul>
                                <p></p>
                                <ul>
                                    <li>
                                        <p><b>CSS (cascading style sheets): </b>The<b> </b>language used to create the
                                            layout, color, and overall style of the pages you create with HTML.</p>
                                    </li>
                                </ul>
                                <p></p>
                                <ul>
                                    <li>
                                        <p><b>JavaScript: </b>The programming language that determines what the page will
                                            do.</p>
                                    </li>
                                </ul>
                                <p></p>
                                <p>HTML, CSS, and JavaScript work together to determine the look and functionality of the
                                    page.&nbsp;</p>
                                <p><b>How to get started: </b>If you want to learn HTML, JavaScript, and CSS, consider
                                    enrolling in an online course like <a
                                        href="https://www.coursera.org/learn/html-css-javascript-for-web-developers"
                                        target="_self" rel="">HTML, CSS, and Javascript for Web Developers from
                                        John Hopkins University</a>. You'll build the foundational knowledge you need to
                                    start a career in web development while earning a certificate for your resume. </p>
                                <h3>Frameworks</h3>
                                <p>Frameworks are platforms for developing software consistently, efficiently, and
                                    accurately. They are tools required for JavaScript and CSS to perform the way you want
                                    them to. Having a solid understanding of them is crucial for creating page structures.
                                </p>
                                <p><b>How to get started: </b>To brush up on your knowledge of popular web development
                                    frameworks, consider learning from an industry leader. For example, you can enroll in
                                    the <a href="https://www.coursera.org/learn/django-web-framework" target="_self"
                                        rel="">Django Web Framework online course</a> taught by the staff at Meta.
                                </p>
                                <h3>Developer tools and software</h3>
                                <p>Software like version control, which tracks and controls changes in your source code, is
                                    critical. It allows you to make changes to your code without starting over.
                                    Understanding how to use various software development tools is a building block to a
                                    successful career.&nbsp;</p>
                                <p><b>How to get started: </b><a
                                        href="https://www.coursera.org/learn/software-design-methods-tools" target="_self"
                                        rel="">The Software Design Methods and Tools course by the University of
                                        Colorado</a> is an excellent way to expand your knowledge of software development
                                    tools. It's 100% online and self-paced. </p>
