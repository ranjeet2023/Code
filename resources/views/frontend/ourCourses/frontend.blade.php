                                <p>A <b>front-end developer</b> creates websites and applications using web languages such
                                    as HTML, CSS, and JavaScript that allow users to access and interact with the site or
                                    app. When you visit a website, the design elements you see were created by a front-end
                                    developer.</p>

                                <h2 id="0-what-does-a-front-end-developer-do" class="mt-5 mb-4">What does a front-end
                                    developer do?&nbsp;
                                </h2>
                                <p class="mt-3">Front-end developers create user interfaces (UI). UI is the graphical
                                    layout of an
                                    application that determines what each part of a site or application does and how it will
                                    look. "I've always found crafting polished user interactions that surprise and delight
                                    users to be the most rewarding and engaging task," says Mari Batilando, a software
                                    engineer at. "In order to do this, you need to both have an eye for
                                    detail and a rock-solid understanding of the platform."</p>
                                <h3 class="mt-5">Front-end developer job description</h3>
                                <p class="mt-3">If someone wanted to build a website, they might hire a front-end
                                    developer to create the
                                    site's layout. The front-end developer determines where to place images, what the
                                    navigation should look like, and how to present the site. Much of their work involves
                                    ensuring the appearance and layout of the site or application is easy to navigate and
                                    intuitive for the user.</p>

